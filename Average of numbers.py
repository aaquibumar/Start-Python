#enter two floating point numbers and finds the average of those values

x = float(input("Please Enter the First Value = "))
y = float(input("Please Enter the Second Value = "))

sum = x + y

avg = sum / 2

flooravgoftwo = avg // 2

print('The sum of {0} and {1} = {2}'.format(x,y,sum))
print('The Average of {0} and {1} = {2}'.format(x,y,avg))
print('The Floor Average of {0} and {1} = {2}'.format(x,y,flooravgoftwo))