from datetime import datetime as syd
dt = syd.now()

print('Date = ',dt)
print('Short Date = ',dt.date())
print('Year = ',dt.year)
print('Month = ',dt.month)
print('Day =',dt.day)
print('Time =',dt.time())
print('Hour =',dt.hour)
print('Minute = ',dt.minute)
print('Second = ',dt.second)
print('Microsecond = ',dt.microsecond)
print('Weekday Number =',dt.weekday())